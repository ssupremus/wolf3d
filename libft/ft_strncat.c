#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t j;
	size_t x;

	i = 0;
	j = ft_strlen(s1);
	x = j + n;
	while (*(s2 + i) && j < x)
	{
		*(s1 + j) = *(s2 + i);
		i++;
		j++;
	}
	*(s1 + j) = '\0';
	return (s1);
}
